package com.police.testing.pojo;

public class StaticDataSimulateExam {
	
	private String departmentName;
	
	private Integer simulateExamCount;
	
	private Integer viewExamCount;

	private Integer sumCount;
	
	public String getDepartmentName() {
		return departmentName;
	}

	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}

	public Integer getSimulateExamCount() {
		return simulateExamCount;
	}

	public void setSimulateExamCount(Integer simulateExamCount) {
		this.simulateExamCount = simulateExamCount;
	}

	public Integer getViewExamCount() {
		return viewExamCount;
	}

	public void setViewExamCount(Integer viewExamCount) {
		this.viewExamCount = viewExamCount;
	}

	public Integer getSumCount() {
		return sumCount;
	}

	public void setSumCount(Integer sumCount) {
		this.sumCount = sumCount;
	}
}
